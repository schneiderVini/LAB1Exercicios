
public class OperacaoAritmetica{
    
    public double soma(double n1, double n2) {
        return n1 + n2;
    }
    public double soma(double n1, double n2, double n3) {
        return n1 + n2 + n3;
    }
    public int soma(int n1, int n2){
        return n1 + n2;
    }
    public int soma(int n1, int n2, int n3){
        return n1 + n2 + n3;
    }
    public double media(double n1, double n2) {
        return (n1 + n2)/ 2 ;
    }
    public double media(double n1, double n2, double n3) {
        return (n1 + n2 +  n3)/ 3 ;
    }
    public double divisao(double n1, double n2) {
        return n1 / n2;
    }
     public int divisao(int n1, int n2) {
        return n1 / n2;
    }
    public double subtracao(double n1, double n2) {
        return n1 - n2;
    }
    public double multiplicacao(double n1, double n2) {
        return n1 * n2;
    }
     public double multiplicacao(double n1, double n2, double n3) {
        return n1 * n2 * n3;
    }
    public double potencia(double p) {
        return Math.pow(p,2);
    }
    public double potencia(double p, int p2) {
        return Math.pow(p,p2);
    }
    
    
    
    
    
    
    
    
    
    
}
